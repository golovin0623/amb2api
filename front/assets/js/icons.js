  /** 创建 SVG 节点 */
  function create(name, opts) {
    opts = opts || {};
    const path = PATHS[name];
    if (!path) {
      // 未知图标：返回 ?
      const span = document.createElement('span');
      span.textContent = '◌';
      span.style.cssText = 'display:inline-block;width:1em;height:1em;text-align:center;color:currentColor;opacity:.5';
      span.title = 'Icon ' + name + ' 未注册';
      return span;
    }
    const svg = document.createElementNS('http://www.w3.org/2000/svg', 'svg');
    const size = opts.size || 16;
    svg.setAttribute('xmlns', 'http://www.w3.org/2000/svg');
    svg.setAttribute('viewBox', '0 0 24 24');
    svg.setAttribute('width', size);
    svg.setAttribute('height', size);
    svg.setAttribute('fill', 'none');
    svg.setAttribute('stroke', 'currentColor');
    svg.setAttribute('stroke-width', opts.strokeWidth || '1.75');
    svg.setAttribute('stroke-linecap', 'round');
    svg.setAttribute('stroke-linejoin', 'round');
    svg.setAttribute('aria-hidden', 'true');
    svg.classList.add('icon', 'icon-' + name);
    if (opts.class) {
      String(opts.class).split(/\s+/).forEach((c) => c && svg.classList.add(c));
    }
    svg.innerHTML = path;
    return svg;
  }

  /** 把元素的 [data-icon] 属性替换为对应 SVG */
  function render(root) {
    root = root || document;
    const nodes = root.querySelectorAll('[data-icon]:not([data-icon-rendered])');
    nodes.forEach((el) => {
      const name = el.getAttribute('data-icon');
      const sizeAttr = el.getAttribute('data-icon-size');
      const size = sizeAttr ? parseInt(sizeAttr, 10) : (el.dataset.iconSize ? parseInt(el.dataset.iconSize, 10) : undefined);
      const svg = create(name, { size: size });
      // 如果元素是 <i> 或空容器，直接清空内容然后注入；
      // 如果元素已经有文字内容（按钮文字），把 SVG 插到首位
      if (el.firstChild) {
        el.insertBefore(svg, el.firstChild);
      } else {
        el.appendChild(svg);
      }
      el.setAttribute('data-icon-rendered', '1');
      el.classList.add('with-icon');
    });
  }

  /** 强制重新渲染（用于动态变更 data-icon） */
  function refresh(el) {
    if (!el) return;
    const old = el.querySelector('svg.icon');
    if (old) old.remove();
    el.removeAttribute('data-icon-rendered');
    render(el.parentNode || document);
  }

  /** 检查可用图标列表（debug） */
  function list() {
    return Object.keys(PATHS).sort();
  }

  window.icons = { create: create, render: render, refresh: refresh, list: list };

  // DOM 就绪后自动渲染
  if (document.readyState === 'loading') {
    document.addEventListener('DOMContentLoaded', () => render(document));
  } else {
    render(document);
  }

  // 监听 DOM 变化，自动渲染新插入的图标
  if (typeof MutationObserver !== 'undefined') {
    const mo = new MutationObserver((mutations) => {
      for (const m of mutations) {
        if (m.type === 'childList') {
          m.addedNodes.forEach((node) => {
            if (node.nodeType === 1) {
              if (node.hasAttribute && node.hasAttribute('data-icon')) {
                render(node.parentNode || document);
              } else if (node.querySelectorAll) {
                render(node);
              }
            }
          });
        }
      }
    });
    if (document.body) {
      mo.observe(document.body, { childList: true, subtree: true });
    } else {
      document.addEventListener('DOMContentLoaded', () => {
        mo.observe(document.body, { childList: true, subtree: true });
      });
    }
  }
